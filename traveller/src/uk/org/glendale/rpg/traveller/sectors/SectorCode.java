package uk.org.glendale.rpg.traveller.sectors;

/**
 * Codes given to each sector.
 * 
 * Co : Core sector, based on 'official' UWP data.
 * Ba : Barren sector, very few life bearing worlds.
 * Fe : Fertile sector, large bonus for Earth-like worlds.
 * Lo : Low level of colonisation
 * Hi : High level of colonisation
 * 
 * @author Samuel Penn
 */
public enum SectorCode {
	Co, Ba, Fe, Lo, Hi; 
}
